def load_prompts() -> dict:
    """prompts.json をデフォルトにマージして返す（キー単位でフォールバック）"""
    data = {}
    if os.path.exists(PROMPTS_FILE):
        try:
            with open(PROMPTS_FILE, encoding="utf-8") as f:
                data = json.load(f)
        except Exception:
            data = {}
    return {k: data.get(k) or v for k, v in DEFAULT_TEMPLATES.items()}


def save_prompts(templates: dict) -> str:
    """プロンプト設定一式を prompts.json に保存（＝ユーザーの既定。出荷時デフォルトは不変）"""
    keep = {k: templates.get(k, DEFAULT_TEMPLATES[k]) for k in DEFAULT_TEMPLATES}
    with open(PROMPTS_FILE, "w", encoding="utf-8") as f:
        json.dump(keep, f, ensure_ascii=False, indent=2)
    return PROMPTS_FILE
